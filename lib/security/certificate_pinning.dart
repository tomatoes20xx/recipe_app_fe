import 'dart:io';

import 'package:http/http.dart' as http;
import 'package:http/io_client.dart';

/// Builds an [http.Client] that only trusts the ISRG Root X1 CA.
///
/// All Railway environments (testing + prod) use Let's Encrypt certificates
/// which chain to ISRG Root X1. By pinning this root we reject any cert not
/// signed by that CA, defeating MITM attacks that rely on rogue/compromised CAs.
///
/// VERIFY this certificate before shipping:
///   openssl s_client -connect recipeappbe-testing.up.railway.app:443 -showcerts
/// Expected ISRG Root X1 SHA-256 fingerprint:
///   96:BC:EC:06:26:49:76:F3:74:60:77:9A:CF:28:C5:A7:
///   CF:E8:A3:C0:AA:E1:1A:8F:FC:EE:05:C0:BD:DF:08:C6
http.Client buildPinnedClient() {
  final context = SecurityContext(withTrustedRoots: false);
  context.setTrustedCertificatesBytes(_isrgRootX1Pem.codeUnits);
  return IOClient(HttpClient(context: context));
}

